/**
  ******************************************************************************
  * @file    taxi.h
  * @brief   자율주행 택시 요금 산출 및 오도메트리(Odometry) 시스템 API 명세
  * @details 휠 엔코더 센서의 Tick 펄스를 기반으로 물리적 이동 거리를 추정하고,
  * 이를 택시 과금 비즈니스 로직과 매핑하는 매크로 및 함수 프로토타입을 정의합니다.
  ******************************************************************************
  */
#ifndef TAXI_H
#define TAXI_H

#include "main.h"

/* ------------------------------------------------------------------------
 * [비즈니스 로직] 택시 요금 체계 (Configurable Parameters)
 * ------------------------------------------------------------------------ */
#define BASE_FARE        4800     // 기본요금 설정 (4800원)
#define FARE_PER_UNIT    50       // 단위 거리당 추가 요금 (50원)
#define DISTANCE_UNIT_CM 20.0f    // 추가 요금이 부과되는 단위 이동 거리 (20cm)

/* ------------------------------------------------------------------------
 * [물리 역학] 차륜 오도메트리(Odometry) 상수
 * @note   물리적 기구부 스펙: 바퀴 지름(D) = 6.5cm, 엔코더 디스크 슬릿 수 = 20개
 * @math   1회전 둘레(Circumference) = D * PI = 6.5 * 3.14159 = 20.42 cm
 * 1 Tick 당 이동 거리 = 20.42 cm / 20 슬릿 = 1.021 cm
 * ------------------------------------------------------------------------ */
#define DIST_PER_TICK_CM 1.021f   // 외부 인터럽트 1회(1 Tick) 발생 시 전진하는 물리적 거리

/* 외부 모듈(main.c, freertos.c)에서 접근 가능한 제어 및 Task 함수 선언 */
void TaxiMeter_Init(void);             // 요금 미터기 및 I2C LCD 하드웨어 초기화
void TaxiMeter_AddLeftTick(void);      // 좌측 휠 엔코더 Tick 카운트 증가 (EXTI ISR 전용)
void TaxiMeter_AddRightTick(void);     // 우측 휠 엔코더 Tick 카운트 증가 (EXTI ISR 전용)

/**
 * @brief 요금 계산 및 UI 갱신을 담당하는 FreeRTOS 스레드(Task) 진입점
 */
void TaxiMeter_Run(void);              

#endif /* TAXI_H */
